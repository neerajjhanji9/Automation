package com.matrix.shriniwas.corejava.polymorphism.overloading;

public class Overloading4 {

    public static void main(String args) {
        System.out.println("I am in main method with String");
    }

    public static void main(int age) {
        System.out.println("I am in main method with int");
    }

    public static void main(String[] args) {
        System.out.println("I am in main method with String []");
    }
}
