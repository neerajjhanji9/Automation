package com.matrix.ajaykumar.corejava.variables;

public class StaticVariables {

    //Static variables
    static int age=10;

    //Instance method
    public void getData(){
        //Instance Area-Direct Access
        System.out.println(age);

    }


    //Static methid
    public static void getInfo(){
        //Static Area-Direct Access
        System.out.println(age);

    }
}
